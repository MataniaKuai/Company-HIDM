package com.hidm.importexport.vo;

import java.math.BigDecimal;

import com.hidm.importexport.entity.ImportItem;

public class ImportItemVo extends ImportItem {
    private String goodsCodeName;
    
    private String irNo;
    
    private String declarationNo;
    
    public String getDeclarationNo() {
        return declarationNo;
    }

    public void setDeclarationNo(String declarationNo) {
        this.declarationNo = declarationNo;
    }

    private BigDecimal batchQty;
    
    public String getGoodsCodeName() {
        return goodsCodeName;
    }

    public void setGoodsCodeName(String goodsCodeName) {
        this.goodsCodeName = goodsCodeName;
    }

    public String getIrNo() {
        return irNo;
    }

    public void setIrNo(String irNo) {
        this.irNo = irNo;
    }

    public BigDecimal getBatchQty() {
        return batchQty;
    }

    public void setBatchQty(BigDecimal batchQty) {
        this.batchQty = batchQty;
    }
    
}
