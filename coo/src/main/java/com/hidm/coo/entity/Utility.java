package com.hidm.coo.entity;

import java.util.Date;

public class Utility {
    private Long id;
    
    private String utilityNo;
    
    private String utilityGroup;
    
    private String utilityName;
    
    private String utilityUnit;
    
    private String coefficient;
    
    private String utilityPrice;
    
    private Date addTime;
    
    private String addUserId;
    
    private Date updateTime;
    
    private String updateUserId;
    
    private Integer delFlag;
    
    private Integer isSap;
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUtilityNo() {
        return utilityNo;
    }

    public void setUtilityNo(String utilityNo) {
        this.utilityNo = utilityNo;
    }

    public String getUtilityGroup() {
        return utilityGroup;
    }

    public void setUtilityGroup(String utilityGroup) {
        this.utilityGroup = utilityGroup;
    }

    public String getUtilityName() {
        return utilityName;
    }

    public void setUtilityName(String utilityName) {
        this.utilityName = utilityName;
    }

    public String getUtilityUnit() {
        return utilityUnit;
    }

    public void setUtilityUnit(String utilityUnit) {
        this.utilityUnit = utilityUnit;
    }

    public String getCoefficient() {
        return coefficient;
    }

    public void setCoefficient(String coefficient) {
        this.coefficient = coefficient;
    }

    public String getUtilityPrice() {
        return utilityPrice;
    }

    public void setUtilityPrice(String utilityPrice) {
        this.utilityPrice = utilityPrice;
    }

    public Date getAddTime() {
        return addTime;
    }

    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    public String getAddUserId() {
        return addUserId;
    }

    public void setAddUserId(String addUserId) {
        this.addUserId = addUserId;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public String getUpdateUserId() {
        return updateUserId;
    }

    public void setUpdateUserId(String updateUserId) {
        this.updateUserId = updateUserId;
    }

	public Integer getDelFlag() {
		return delFlag;
	}

	public void setDelFlag(Integer delFlag) {
		this.delFlag = delFlag;
	}

	public Integer getIsSap() {
		return isSap;
	}

	public void setIsSap(Integer isSap) {
		this.isSap = isSap;
	}
    
    
}
